import copy
import numpy as np
import torch
import torch.nn as nn
import torch.optim as optim

from sklearn.cluster import KMeans

DEVICE = torch.device("cuda" if torch.cuda.is_available() else "cpu")

# ================================================================
# PARAMETERS
# ================================================================

INPUT_SIZE = NUM_FEATURES

HIDDEN_SIZE = 32

NUM_LAYERS = 2

OUTPUT_SIZE = 1

LOCAL_EPOCHS = 5

LR = 1e-3

MU = 0.01                 # FedProx coefficient

SPARSITY = 0.15           # Top-k update

NUM_CLIENTS = 10

NUM_CLUSTERS = 4

# ================================================================
# LIGHTWEIGHT GRU
# ================================================================

class HPFL_GRU(nn.Module):

    def __init__(self):

        super().__init__()

        self.gru = nn.GRU(
            INPUT_SIZE,
            HIDDEN_SIZE,
            NUM_LAYERS,
            batch_first=True
        )

        self.fc = nn.Linear(HIDDEN_SIZE, OUTPUT_SIZE)

    def forward(self, x):

        out, _ = self.gru(x)

        out = out[:, -1]

        out = self.fc(out)

        return out.squeeze()

# ================================================================
# FEDPROX LOSS
# ================================================================

def fedprox_loss(model,
                 global_model,
                 pred,
                 target):

    mse = nn.MSELoss()(pred, target)

    prox = 0

    for w, wg in zip(model.parameters(),
                     global_model.parameters()):

        prox += torch.sum((w - wg) ** 2)

    return mse + MU * prox

# ================================================================
# LOCAL TRAINING
# ================================================================

def local_train(global_model,
                loader):

    model = copy.deepcopy(global_model)

    model.to(DEVICE)

    optimizer = optim.Adam(
        model.parameters(),
        lr=LR
    )

    model.train()

    for epoch in range(LOCAL_EPOCHS):

        for x, y in loader:

            x = x.to(DEVICE)

            y = y.to(DEVICE)

            optimizer.zero_grad()

            pred = model(x)

            loss = fedprox_loss(
                model,
                global_model,
                pred,
                y
            )

            loss.backward()

            optimizer.step()

    return model

# ================================================================
# PARAMETER DIFFERENCE
# ================================================================

def model_difference(local_model,
                     global_model):

    diff = {}

    for (k, v1), (_, v2) in zip(
            local_model.state_dict().items(),
            global_model.state_dict().items()):

        diff[k] = v1 - v2

    return diff

# ================================================================
# TOP-K SPARSIFICATION
# ================================================================

def sparsify(update):

    sparse = {}

    for k, v in update.items():

        flat = v.view(-1)

        total = flat.numel()

        k_keep = max(1,
                     int(total * SPARSITY))

        idx = torch.topk(
            flat.abs(),
            k_keep
        ).indices

        mask = torch.zeros_like(flat)

        mask[idx] = 1

        sparse[k] = (flat * mask).view_as(v)

    return sparse

# ================================================================
# INT8 COMPRESSION
# ================================================================

def quantize(update):

    compressed = {}

    scales = {}

    for k, v in update.items():

        scale = v.abs().max() / 127

        if scale == 0:
            scale = 1e-8

        q = (v / scale).round()

        q = q.clamp(-127,127)

        compressed[k] = q.to(torch.int8)

        scales[k] = scale

    return compressed, scales

# ================================================================
# DEQUANTIZATION
# ================================================================

def dequantize(compressed,
               scales):

    recovered = {}

    for k in compressed:

        recovered[k] = (
            compressed[k].float()
            * scales[k]
        )

    return recovered

# ================================================================
# WORKLOAD FEATURES
# ================================================================

def workload_statistics(loader):

    values = []

    for x,_ in loader:

        values.extend(
            x[:,:,-1].reshape(-1).numpy()
        )

    values = np.array(values)

    mean = np.mean(values)

    var = np.var(values)

    acorr = np.corrcoef(
        values[:-1],
        values[1:]
    )[0,1]

    return np.array([
        mean,
        var,
        acorr
    ])

# ================================================================
# CLIENT CLUSTERING
# ================================================================

def cluster_clients(loaders):

    stats=[]

    for loader in loaders:

        stats.append(
            workload_statistics(loader)
        )

    stats=np.array(stats)

    km=KMeans(
        n_clusters=NUM_CLUSTERS,
        random_state=42
    )

    labels=km.fit_predict(stats)

    return labels

# ================================================================
# CLUSTER AGGREGATION
# ================================================================

def aggregate_cluster(updates):

    agg={}

    keys=updates[0].keys()

    for k in keys:

        agg[k]=torch.zeros_like(
            updates[0][k]
        )

        for u in updates:

            agg[k]+=u[k]

        agg[k]/=len(updates)

    return agg

# ================================================================
# HIERARCHICAL AGGREGATION
# ================================================================

def hierarchical_aggregation(
        global_model,
        client_updates,
        labels):

    new_state = copy.deepcopy(
        global_model.state_dict()
    )

    for c in np.unique(labels):

        idx = np.where(labels==c)[0]

        cluster=[]

        for i in idx:

            cluster.append(
                client_updates[i]
            )

        avg = aggregate_cluster(cluster)

        for k in new_state:

            new_state[k] += avg[k] / len(np.unique(labels))

    global_model.load_state_dict(
        new_state
    )

    return global_model

# ================================================================
# FEDERATED ROUND
# ================================================================

def federated_round(global_model,
                    loaders):

    local_updates=[]

    labels=cluster_clients(loaders)

    for loader in loaders:

        local_model=local_train(
            global_model,
            loader
        )

        diff=model_difference(
            local_model,
            global_model
        )

        diff=sparsify(diff)

        comp,scale=quantize(diff)

        diff=dequantize(comp,scale)

        local_updates.append(diff)

    global_model = hierarchical_aggregation(
        global_model,
        local_updates,
        labels
    )

    return global_model

