import torch
import torch.nn as nn
import torch.nn.functional as F
from torch.distributions import Categorical

from torch_geometric.nn import GATConv

DEVICE = torch.device("cuda" if torch.cuda.is_available() else "cpu")

# ============================================================
# PARAMETERS
# ============================================================

NODE_FEATURES = 5
HIDDEN = 64
ACTION_SIZE = 4
GAMMA = 0.99
EPS_CLIP = 0.2
PPO_EPOCHS = 4
LR = 3e-4

# ============================================================
# GRAPH ATTENTION NETWORK
# ============================================================

class GATEncoder(nn.Module):

    def __init__(self):

        super().__init__()

        self.gat1 = GATConv(
            NODE_FEATURES,
            HIDDEN,
            heads=4
        )

        self.gat2 = GATConv(
            HIDDEN*4,
            HIDDEN,
            heads=1
        )

    def forward(self,x,edge_index):

        x = F.elu(self.gat1(x,edge_index))

        x = self.gat2(x,edge_index)

        return x

# ============================================================
# PPO POLICY NETWORK
# ============================================================

class PPOAgent(nn.Module):

    def __init__(self):

        super().__init__()

        self.encoder = GATEncoder()

        self.actor = nn.Sequential(

            nn.Linear(HIDDEN,64),

            nn.ReLU(),

            nn.Linear(64,ACTION_SIZE)

        )

        self.critic = nn.Sequential(

            nn.Linear(HIDDEN,64),

            nn.ReLU(),

            nn.Linear(64,1)

        )

    def forward(self,x,edge_index):

        h = self.encoder(x,edge_index)

        logits = self.actor(h)

        value = self.critic(h)

        return logits,value

agent = PPOAgent().to(DEVICE)

optimizer = torch.optim.Adam(
    agent.parameters(),
    lr=LR
)

# ============================================================
# DIGITAL TWIN
# ============================================================

class DigitalTwin:

    def __init__(self,n_nodes):

        self.n_nodes=n_nodes

        self.reset()

    def reset(self):

        self.cpu=np.random.rand(self.n_nodes)

        self.memory=np.random.rand(self.n_nodes)

        self.queue=np.random.rand(self.n_nodes)

        return self.state()

    def state(self):

        return np.stack([

            self.cpu,

            self.memory,

            self.queue,

            np.random.rand(self.n_nodes),

            np.random.rand(self.n_nodes)

        ],axis=1)

    def step(self,action):

        reward=0

        for i,a in enumerate(action):

            if a==0:

                self.cpu[i]*=0.95

            elif a==1:

                self.memory[i]*=0.95

            elif a==2:

                self.queue[i]*=0.80

            else:

                pass

        reward=-(

            self.cpu.mean()

            +self.memory.mean()

            +self.queue.mean()

        )

        done=False

        return self.state(),reward,done

env=DigitalTwin(20)

# ============================================================
# ACTION SELECTION
# ============================================================

def choose_action(state):

    x=torch.FloatTensor(state).to(DEVICE)

    logits,value=agent(x,edge_index.to(DEVICE))

    probs=F.softmax(logits,dim=1)

    dist=Categorical(probs)

    action=dist.sample()

    logprob=dist.log_prob(action)

    return action.cpu(),logprob,value

# ============================================================
# PPO UPDATE
# ============================================================

def ppo_update(states,
               actions,
               rewards,
               old_log):

    returns=[]

    R=0

    for r in rewards[::-1]:

        R=r+GAMMA*R

        returns.insert(0,R)

    returns=torch.tensor(returns,dtype=torch.float32).to(DEVICE)

    for _ in range(PPO_EPOCHS):

        loss_total=0

        for s,a,ret,lp in zip(

            states,

            actions,

            returns,

            old_log

        ):

            s=torch.FloatTensor(s).to(DEVICE)

            logits,value=agent(s,edge_index.to(DEVICE))

            probs=F.softmax(logits,dim=1)

            dist=Categorical(probs)

            new_lp=dist.log_prob(a.to(DEVICE))

            ratio=torch.exp(new_lp-lp.to(DEVICE))

            adv=ret-value.mean()

            s1=ratio*adv

            s2=torch.clamp(

                ratio,

                1-EPS_CLIP,

                1+EPS_CLIP

            )*adv

            actor=-torch.min(s1,s2).mean()

            critic=F.mse_loss(

                value.mean(),

                ret

            )

            loss=actor+0.5*critic

            optimizer.zero_grad()

            loss.backward()

            optimizer.step()

            loss_total+=loss.item()

    return loss_total

# ============================================================
# RESOURCE ALLOCATION
# ============================================================

def allocate_resources(action,state):

    cpu=state[:,0]

    mem=state[:,1]

    queue=state[:,2]

    for i,a in enumerate(action):

        if a==0:

            cpu[i]=max(cpu[i]-0.1,0)

        elif a==1:

            mem[i]=max(mem[i]-0.1,0)

        elif a==2:

            queue[i]=max(queue[i]-0.2,0)

        else:

            cpu[i]+=0.02

    state[:,0]=cpu

    state[:,1]=mem

    state[:,2]=queue

    return state

