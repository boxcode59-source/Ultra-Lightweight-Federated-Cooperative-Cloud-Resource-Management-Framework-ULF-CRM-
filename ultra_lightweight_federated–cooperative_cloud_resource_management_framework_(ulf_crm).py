# ==============================================================
# STATE SPACE BLOCK
# ==============================================================

class StateSpaceBlock(nn.Module):

    def __init__(self):

        super().__init__()

        self.A = nn.Linear(HIDDEN_DIM,HIDDEN_DIM)

        self.B = nn.Linear(HIDDEN_DIM,HIDDEN_DIM)

    def forward(self,x):

        h = torch.tanh(self.A(x))

        y = self.B(h)

        return y

# ==============================================================
# TEMPORAL ENCODER
# ==============================================================

class TemporalEncoder(nn.Module):

    def __init__(self):

        super().__init__()

        self.gru = nn.GRU(

            INPUT_DIM,

            HIDDEN_DIM,

            batch_first=True

        )

    def forward(self,x):

        out,_ = self.gru(x)

        return out[:,-1]

# ==============================================================
# GRAPH FUSION
# ==============================================================

class GraphFusion(nn.Module):

    def __init__(self):

        super().__init__()

        self.fc = nn.Linear(HIDDEN_DIM,HIDDEN_DIM)

    def forward(self,x):

        return F.relu(self.fc(x))

# ==============================================================
# FORECAST NETWORK
# ==============================================================

class ForecastNet(nn.Module):

    def __init__(self):

        super().__init__()

        self.encoder = TemporalEncoder()

        self.ssm = StateSpaceBlock()

        self.graph = GraphFusion()

        self.head = nn.Linear(

            HIDDEN_DIM,

            OUTPUT_DIM

        )

    def forward(self,x):

        x = self.encoder(x)

        x = self.ssm(x)

        x = self.graph(x)

        y = self.head(x)

        return y.squeeze()

