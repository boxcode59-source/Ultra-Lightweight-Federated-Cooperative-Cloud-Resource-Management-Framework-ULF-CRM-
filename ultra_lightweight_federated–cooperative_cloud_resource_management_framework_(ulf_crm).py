class Server:

    def __init__(self,name,nodes,capacity,latency):

        self.name=name

        self.nodes=nodes

        self.capacity=capacity

        self.latency=latency

        self.load=np.zeros(nodes)

        self.memory=np.zeros(nodes)

    def allocate(self,cpu,mem):

        idx=np.argmin(self.load)

        if self.load[idx]+cpu<=self.capacity:

            self.load[idx]+=cpu

            self.memory[idx]+=mem

            return True,idx

        return False,-1

