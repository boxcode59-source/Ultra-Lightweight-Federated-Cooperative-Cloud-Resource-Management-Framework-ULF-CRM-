# ================================================================
# OUTLIER REMOVAL (IQR)
# ================================================================

def remove_outliers(df):

    for col in FEATURE_COLUMNS:

        Q1 = df[col].quantile(0.25)

        Q3 = df[col].quantile(0.75)

        IQR = Q3 - Q1

        low = Q1 - 1.5*IQR

        high = Q3 + 1.5*IQR

        median = df[col].median()

        df.loc[df[col] < low, col] = median

        df.loc[df[col] > high, col] = median

    return df

