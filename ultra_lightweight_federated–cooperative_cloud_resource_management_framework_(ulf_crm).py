class AutoEncoder(nn.Module):

    def __init__(self):

        super().__init__()

        self.encoder = nn.Sequential(

            nn.Linear(INPUT_DIM,64),

            nn.ReLU(),

            nn.Linear(64,32),

            nn.ReLU(),

            nn.Linear(32,LATENT_DIM)

        )

        self.decoder = nn.Sequential(

            nn.Linear(LATENT_DIM,32),

            nn.ReLU(),

            nn.Linear(32,64),

            nn.ReLU(),

            nn.Linear(64,INPUT_DIM)

        )

    def forward(self,x):

        z = self.encoder(x)

        out = self.decoder(z)

        return out

