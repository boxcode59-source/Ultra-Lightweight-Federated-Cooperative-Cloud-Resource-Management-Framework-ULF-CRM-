# ==============================================================
# KNOWLEDGE DISTILLATION
# ==============================================================

def distillation_loss(

    student,

    teacher,

    target

):

    mse = F.mse_loss(

        student,

        target

    )

    kd = F.mse_loss(

        student,

        teacher.detach()

    )

    return 0.7*mse + 0.3*kd

# ==============================================================
# SELF SUPERVISED LOSS
# ==============================================================

def ssl_loss(z1,z2):

    z1 = F.normalize(z1,dim=1)

    z2 = F.normalize(z2,dim=1)

    sim = torch.mm(

        z1,

        z2.t()

    )/TEMPERATURE

    labels = torch.arange(

        sim.size(0)

    ).to(DEVICE)

    return F.cross_entropy(

        sim,

        labels

    )

